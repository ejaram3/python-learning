# Tema: Módulos
""" 
Un módulo es un archivo que contiene un conjunto de código o conjunto de aplicaciones que se pueden incluir en un aplicación
"""

# Creación de un modulo
def crea_nombre_completo(nombre, apellido):
    return nombre +" "+apellido

def suma_dos_numeros(a, b):
    return a + b
def resta_dos_numeros(a, b):
    return a - b
def mult_dos_numeros(a, b):
    return a * b
def div_dos_numeros(a, b):
    return a / b